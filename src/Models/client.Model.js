const { model, Schema } = require('mongoose');

const clientSchema = new Schema({
    name: {
        type: String,
        required: true
    },
    lastname: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    gender: {
        type: String,
        required: true
    },
    age: {
        type: Number,
        required: true
    }
});

const Client = model('Client', clientSchema);

module.exports = { 
    Client
};